// if文の基礎

// 文字列
var str = 'a';
// 等しい
if( str == 'a' ){
	alert('str is a.');		// こちら
}else{
	alert('str is not a.');
}
// 等しくない
if( str != 'b' ) alert('str is not b.');


// 数値
var num = 10;
if( num == 10 ) alert('num is 10.');
// 等しい ==
// 等しくない !=
// 不等号 <、<=、>=、>


// 複数条件
// and → &&
// or  → ||